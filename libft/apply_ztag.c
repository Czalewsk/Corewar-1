#include "ft_printf.h"

void		apply_ztag(char **str)
{
	int		i;

	i = 0;
	if (**str != ' ')
		return ;
	while ((*str)[i] && (*str)[i] == ' ')
		(*str)[i++] = '0';
	if ((*str)[i] == '-' || (*str)[i] == '+')
	{
		(*str)[0] = (*str)[i];
		(*str)[i] = '0';
	}
	if ((*str)[i] == '0' && ((*str)[i + 1] == 'x' || (*str)[i + 1] == 'X'))
	{
		(*str)[1] = (*str)[i + 1];
		(*str)[i + 1] = '0';
	}
}
