#include "asm.h"

void			get_arg(int *arg, char *av, int *nb_arg)
{
	if (!ft_strcmp(av, "-d"))
		*arg = *arg | 1;
	else if (!ft_strcmp(av, "-r"))
		*arg = *arg | 2;
	++(*nb_arg);
}
